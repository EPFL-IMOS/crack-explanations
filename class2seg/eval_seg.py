def measure_crack(crack):
    """Measures the width of a single crack represented as a binary image, using the method from
    Carrasco et al. Image-Based Automated Width Measurement of Surface Cracking. Sensors (2021)."""

    max_width = 0

    # 1. Skeletonize
    skel = skeletonize(crack)

    # 2. Get central points by clustering skeleton points with k-means
    pts = np.column_stack(np.where(skel.transpose() == 1))

    d_min = float("+inf")
    if len(pts) < 2:
        return 0
    for k in range(2, len(pts)+1):
        km = KMeans(n_clusters=k, n_init="auto").fit(pts)
        pred = np.unique(km.predict(pts))
        c = km.cluster_centers_[pred]
        min_dist = euclidean_distances(c)
        np.fill_diagonal(min_dist, float("inf"))
        min_dist = min_dist.min()
        if min_dist >= d_min:
            break
        d_min = min_dist

    k = k-1
    central_points = KMeans(n_clusters=k, n_init="auto").fit(pts).cluster_centers_

    w = 20
    for pt in central_points:
        # 3. Fit ellipse on 40x40 region around central point of skeleton
        x, y = int(pt[0]), int(pt[1])
        x0 = max(0, x-w)
        x1 = min(crack.shape[0], x+w)
        y0 = max(0, y-w)
        y1 = min(crack.shape[1], y+w)
        region = crack[y0:y1,x0:x1]
        region = (region*255).astype(np.uint8)

        contours, _ = cv2.findContours(region, cv2.RETR_TREE, cv2.CHAIN_APPROX_SIMPLE)
        if contours is None or len(contours) == 0 or contours[0].shape[0] < 5:
            continue
        minEllipse = cv2.fitEllipse(contours[0])
        angle = minEllipse[2] * math.pi / 180

        # 4. Calculate maximum width in normal direction profile line
        profile = profile_line(region, [w*(1-math.sin(angle)), w*(1-math.cos(angle))], [w*(1+math.sin(angle)), w*(1+math.cos(angle))], mode="constant")
        width = 0
        for p in profile:
            if p > 0:
                width += 1
            elif width > 0:
                break
        max_width = max(max_width, width)

    return max_width


def cracks_per_patch_max_width(cam, min_area=100):
    """Computes the number and maximum width of cracks in a binary image."""

    cam = (cam*255).astype(np.uint8)

    # find all of the connected components (white blobs in your image).
    # im_with_separated_blobs is an image where each detected blob has a different pixel value ranging from 1 to nb_blobs - 1.
    _, im_with_separated_blobs, stats, _ = cv2.connectedComponentsWithStats(cam)
    # stats (and the silenced output centroids) gives some information about the blobs. See the docs for more information.
    # here, we're interested only in the size of the blobs, contained in the last column of stats.
    sizes = stats[:, -1]
    # the following lines result in taking out the background which is also considered a component, which I find for most applications to not be the expected output.
    # you may also keep the results as they are by commenting out the following lines. You'll have to update the ranges in the for loop below.
    sizes_with_indices = sorted(zip(sizes[1:], range(1, len(sizes))), reverse=True)

    cpp = 0
    for size, _ in sizes_with_indices:
        if size < min_area:
            break
        cpp += 1

    max_width = 0
    # for each crack
    for _, idx in sizes_with_indices[:cpp]:
        crack = np.where(im_with_separated_blobs == idx, 1, 0)
        crack_width = measure_crack(crack)
        max_width = max(max_width, crack_width)

    return cpp, max_width


def score_images(
    dataset: str,
    image_path: os.PathLike,
    data_path: os.PathLike = "/home/florent/imos/data/DIC_crack_dataset",
    fold: str = "val",
    post_method: str = "simple",
    model: str = "vgg11-128-alphabeta",
    correct_only: bool = False,
    imagesize: int = 256,
    **kwargs,
) -> float:

    if isinstance(image_path, str):
        image_path = Path(image_path)

    Dataset = data.__dict__[dataset]

    dataset = Dataset(
        root=data_path,
        fold=fold,
        transform=get_val_transform(size=imagesize),
        segmentation=True,
        positive_only=True
    )

    # binary confusion matrix
    cm = np.zeros((2,2), dtype=int)
    n = 0

    # crack severity metrics
    cpp_true_a, area_true_a, width_true_a = [], [], []
    cpp_pred_a, area_pred_a, width_pred_a = [], [], []
    t, t0 = 0, 0

    for i, (_, true_mask, id_im) in enumerate(tqdm(dataset)):

        true_mask = true_mask.numpy()

        if true_mask.max() > 0 and ((not correct_only) or (y_pred[fold][model][i] == 1)):

            # for multi-class
            true_mask = (true_mask / true_mask.max()).astype(int)
            n += 1
            cam_path = image_path / id_im / "cam_grayscale.npy"
            cam = np.load(cam_path)
            cam_m = cam
            t0 = time.time()
            for method in post_method.split("+"):
                if method == "final":
                    cam_m = closing_post(cam_m, radius=5)
                    cam_m = area_opening_post(cam_m, min_area=50)
                    cam_m = closing_post(cam_m, radius=25)
                else:
                    cam_m = post_factory(method=method)(cam=cam_m, **kwargs)
            t += time.time() - t0
            # if n > 100:
            #     break
            if not os.path.exists(image_path / id_im):
                os.makedirs(image_path / id_im)
            plt.imsave(image_path / id_im / f"cam_{post_method}.png", cam_m, cmap="gray")

            assert true_mask.max() <= 1 and cam_m.max() <= 1
            cm += confusion_matrix(true_mask.flatten(), cam_m.flatten())

            # cracks per patch and maximum width
            cpp_true, width_true = cracks_per_patch_max_width(true_mask, min_area=50)
            cpp_pred, width_pred = cracks_per_patch_max_width(cam_m, min_area=50)
            cpp_true_a.append(int(cpp_true))
            cpp_pred_a.append(int(cpp_pred))
            width_true_a.append(int(width_true))
            width_pred_a.append(int(width_pred))

            # cracked area
            area_true = true_mask.sum()
            area_pred = cam_m.sum()
            area_true_a.append(int(area_true))
            area_pred_a.append(int(area_pred))

    print(f"TIME PER IMAGE: {t/n}")

    tp = cm[1,1]
    fp = cm[0,1]
    fn = cm[1,0]
    precision = tp / (tp + fp)
    recall = tp / (tp + fn)
    f1 = 2*tp / (2*tp + fp + fn)
    jaccard = tp / (tp + fp + fn)

    return {
        "f1_score": f1,
        "precision": precision,
        "recall": recall,
        "jaccard_score": jaccard,
        "cpp_true": cpp_true_a,
        "area_true": area_true_a,
        "width_true": width_true_a,
        "cpp_pred": cpp_pred_a,
        "area_pred": area_pred_a,
        "width_pred": width_pred_a,
        "n": n
    }


def compute_all_metrics(**kwargs):

    type_method = kwargs["image_path"].removesuffix("/").split("/")[-1] #[-3]

    method_desc = []

    if "post_method" not in kwargs:
        kwargs["post_method"] = "simple"

    for method in kwargs["post_method"].split("+"):

        if method == "simple":
            threshold = kwargs["threshold"] if "threshold" in kwargs else 1e-6
            method_desc.append(f"simple_{threshold}")

        elif method == "gmm":
            kernel_size = kwargs["kernel_size"] if "kernel_size" in kwargs else 3
            n_components = kwargs["n_components"] if "n_components" in kwargs else 3
            params = f"kernel_{kernel_size}_comp_{n_components}"
            method_desc.append(f"gmm_{params}")

        elif method == "bmm":
            kernel_size = kwargs["kernel_size"] if "kernel_size" in kwargs else 3
            params = f"kernel_{kernel_size}"
            method_desc.append(f"bmm_{params}")

        elif method == "quantile":
            quantile = kwargs["quantile"] if "quantile" in kwargs else 0.8
            method_desc.append(f"quantile_{quantile}")

        elif method == "otsu":
            method_desc.append("otsu")

        elif method == "closing":
            kernel = kwargs["structuring_element"] if "structuring_element" in kwargs else "ellipse"
            radius = kwargs["radius"] if "radius" in kwargs else 3
            iterations = kwargs["iterations"] if "iterations" in kwargs else 1
            if iterations > 1:
                method_desc.append(f"closing_{kernel}_{radius}_{iterations}")
            else:
                method_desc.append(f"closing_{kernel}_{radius}")

        elif method == "opening":
            kernel = kwargs["structuring_element"] if "structuring_element" in kwargs else "ellipse"
            radius = kwargs["radius"] if "radius" in kwargs else 3
            iterations = kwargs["iterations"] if "iterations" in kwargs else 1
            if iterations > 1:
                method_desc.append(f"opening_{kernel}_{radius}_{iterations}")
            else:
                method_desc.append(f"opening_{kernel}_{radius}")

        elif method == "area_opening":
            min_area = kwargs["min_area"] if "min_area" in kwargs else 250
            method_desc.append(f"area_{min_area}")

        elif method == "threshold":
            threshold = kwargs["threshold"] if "threshold" in kwargs else 0.5
            method_desc.append(f"threshold_{threshold}")

        else:
            method_desc.append(method)

    fold = kwargs["fold"] if "fold" in kwargs else "val"

    output_path = kwargs["image_path"].split("activation_map")[0].removesuffix("/")
    output_dir = f"{output_path}/{type_method}_{'_'.join(method_desc)}_fold_{fold}"
    logger = SummaryWriter(log_dir=output_dir)

    metrics = score_images(**kwargs)

    # if isinstance(kwargs["image_path"], str):
    #     image_path = Path(kwargs["image_path"])
    if "correct_only" in kwargs and kwargs["correct_only"]:
        metrics_path = output_dir + "/metrics_correct_only.json"
    else:
        metrics_path = output_dir + "/metrics.json"
    with open(metrics_path, "w") as f:
        json.dump(metrics, f)

    for metric_name, metric_value in metrics.items():
        if not isinstance(metric_value, list):
            if "correct_only" in kwargs and kwargs["correct_only"]:
                logger.add_scalar(metric_name + "_correct_only", metric_value)
            else:
                logger.add_scalar(metric_name, metric_value)
    logger.close()


